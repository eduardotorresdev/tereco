				A base ritualística do terecô está ligada à encantaria. A encantaria é
				um processo místico em que pessoas somem e são rebatizadas para ajudar
				os pecadores na terra. Os encantados são seres mais evoluídos que descem
				para ajudar as pessoas pela intervenção dos médiuns. No mundo dos
				encantados, não há pecadores e nem santos, os encantados ficam entre o
